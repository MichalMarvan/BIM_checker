// Helper function to get PropertySets for specific IFC version
function getPropertySetsForVersion(ifcVersion) {
    // Normalize version string
    const version = ifcVersion?.toUpperCase() || 'IFC4';

    // Return specific version or fallback to IFC4
    return IFC_PROPERTY_SETS_BY_VERSION[version] || IFC_PROPERTY_SETS_BY_VERSION.IFC4;
}

